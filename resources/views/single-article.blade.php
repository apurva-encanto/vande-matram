                          <p class="card-text">Applicants have to wait years to get a green card for permanent residence in the US. A decision has been taken to give relief to such people. On October 26, the White House Commissioner officially recommended to the government that the Green Card applicants should be given an Employment Authorization Card (EAD) at the earliest. If President Joe Biden gives his stamp of approval, the EAD approval process will begin. This is likely to benefit millions of Indians. Along with this, several key changes have been suggested in the green card application process. As per the current process, candidates for green card must first apply I-140. After that the process reaches a critical stage. At this stage the status adjustment takes place. It is known as I-485. Advance parole of EAD card and other travel documents will be issued at this stage. With this, the candidates can work anywhere in the country. If the latest recommendation comes into effect, the government will have to issue the EAD at the I-140 stage itself. Experts say the recommendation will benefit Indians who currently have to wait for decades for a green card.</p>
